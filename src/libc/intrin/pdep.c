#include "libc/intrin/pdep.h"

/**
 * Parallel bit deposit.
 */
uint64_t(pdep)(uint64_t x, uint64_t mask) {
  uint64_t r, b;
  for (r = 0, b = 1; mask; mask >>= 1, b <<= 1) {
    if (mask & 1) {
      if (x & 1) r |= b;
      x >>= 1;
    }
  }
  return r;
}
